function selectLanguage(lang)
	if lang == "en" then
		stepButtonName = "Step"
		tableauButtonName = "All"
		latexButtonName = "LaTeX"
		dotButtonName = "Dot"
		undoButtonName = "Undo"
		readFileButtonName = "Read"
		disposeButtonName = "Adjust"
		trueLabel = "T"
		falseLabel = "F"
		tableauClosedLabel = "Tableau Closed!"
		fileDisclaimer = "Generated by TableauxProver"
		inputFail = "File not found"
	elseif lang == "pt-br" then
		stepButtonName = "Passo"
		tableauButtonName = "Tableau"
		latexButtonName = "LaTeX"
		dotButtonName = "Dot"
		undoButtonName = "Desfazer"
		readFileButtonName = "Arquivo"
		disposeButtonName = "Ajustar"
		trueLabel = "V"
		falseLabel = "F"
		tableauClosedLabel = "Tableau Fechado!"
		fileDisclaimer = "Gerado pelo TableauxProver"
		inputFail = "Arquivo inexistente"
	end
end
